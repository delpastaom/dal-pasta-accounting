export type Translations = typeof translations.ar;

let currentLang: Lang = (localStorage.getItem('app_lang') as Lang) || 'ar';

export function setLang(lang: Lang) {
  currentLang = lang;
  localStorage.setItem('app_lang', lang);
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  window.dispatchEvent(new CustomEvent('langchange', { detail: lang }));
}

export function getLang(): Lang {
  return currentLang;
}

export function t(key: TranslationKey | string): string {
  return translations[currentLang][key as TranslationKey] || key;
}

